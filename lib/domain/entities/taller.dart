class Taller {
  final int? id;
  final String nombre;
  final String direccion;
  final String telefono;
  final double latitud;
  final double longitud;

  const Taller({
    this.id,
    required this.nombre,
    required this.direccion,
    required this.telefono,
    required this.latitud,
    required this.longitud,
  });
}
